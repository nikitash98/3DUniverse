

import { useState } from "react"
import "./SectorInfo.css"
import data from "../sectorinfo.json"
const SectorInfo = (props) => {
    const [showMoreInfo, setShowMoreInfo] = useState(false)
    const handleHover = () => {
        setShowMoreInfo(true)
        console.log("ABC")
    }
    const handleUnhover = () => {
        setShowMoreInfo(false)
    }
    return <div className= {props.showSectorInfo?'sector_info':"sector_info hidden"} onMouseOver={handleHover} onMouseLeave={handleUnhover}>
        <h1>
            {props.sector}
        </h1>
        <img className="icon" src = "icons/down.svg"/>
        <div className={!showMoreInfo?"explanation_text": "explanation_text text_expand"} >
        <img src ={data[props.sector].img}/>

            <p className="">
                {data[props.sector].text}
            </p>
        </div>

    </div>

}
export default SectorInfo

